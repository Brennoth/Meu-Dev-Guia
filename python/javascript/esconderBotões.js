
/*Esconde o botão do link de navegação da IA*/
function esconderBotaoIa() {
    const liIA = document.querySelector(".li-IA")
    liIA.classList.toggle("open")
}


/*Esconde o botão do link de navegação da Biblioteca*/
function esconderBotaoBiblioteca() {
    const liBibliotecas = document.querySelector(".li-bibliotecas")
    liBibliotecas.classList.toggle("open")
}

function esconderTitleIa() {
    const bibliotecasIa = document.querySelector("#bibliotecas-ia")
    bibliotecasIa.classList.toggle("openTitle")
}


    

